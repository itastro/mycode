package com.bailian.car.param;

import java.io.Serializable;

import lombok.AllArgsConstructor;
import lombok.Builder;
import lombok.NoArgsConstructor;

@AllArgsConstructor
@NoArgsConstructor
@Builder
/**
 * 
 * @ClassName: ApplyBom
 * @Description: bom零部件申请
 * @author itastro
 * @date 2018年5月22日
 *
 */
public class ApplyBom implements Serializable {
	    
	private static final long serialVersionUID = 1L;

	public String getBomNum() {
		return bomNum;
	}

	public void setBomNum(String bomNum) {
		this.bomNum = bomNum;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getBomName() {
		return bomName;
	}

	public void setBomName(String bomName) {
		this.bomName = bomName;
	}

	public String getApplyPerson() {
		return ApplyPerson;
	}

	public void setApplyPerson(String applyPerson) {
		ApplyPerson = applyPerson;
	}

	public String getvSn() {
		return vSn;
	}

	public void setvSn(String vSn) {
		this.vSn = vSn;
	}

	private Integer id;

	private String bomName; // bom名称

	private String bomNum; // 零部件名称

	private String ApplyPerson; // 申请人

	private String vSn; // 车辆编号

}
