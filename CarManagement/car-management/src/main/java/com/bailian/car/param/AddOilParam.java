package com.bailian.car.param;

import java.io.Serializable;

/**
 * 
    * @ClassName: AddOilParam
    * @Description: 油卡填写参数
    * @author itastro
    * @date 2018年6月21日
    *
 */
public class AddOilParam implements Serializable {

	    
	private static final long serialVersionUID = 1L;

	public String getTime() {
		return time;
	}

	public void setTime(String time) {
		this.time = time;
	}

	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getlNo() {
		return lNo;
	}

	public void setlNo(String lNo) {
		this.lNo = lNo;
	}

	public String getvSn() {
		return vSn;
	}

	public void setvSn(String vSn) {
		this.vSn = vSn;
	}

	public String getMm() {
		return mm;
	}

	public void setMm(String mm) {
		this.mm = mm;
	}

	public String getProjectSn() {
		return projectSn;
	}

	public void setProjectSn(String projectSn) {
		this.projectSn = projectSn;
	}

	private Integer id; // 需要填写的那条数据的id

	private String time; // 日期

	private String vSn; // 车辆编号

	private String lNo; // 车牌号

	private String mm; // 项目号

	private String projectSn; // 里程表

}
